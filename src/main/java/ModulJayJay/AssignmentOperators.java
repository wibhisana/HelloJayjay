package ModulJayJay;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;

        a += 5;  // (a = a + 5)
        a -= 3;  // (a = a - 3)
        a *= 2;  // (a = a * 2)
        a /= 3;  // (a = a / 3)
        a %= 2;  // (a = a % 2)

        System.out.println("Nilai akhir a: " + a);
    }
}
